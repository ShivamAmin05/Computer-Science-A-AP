class TwoFloatNumbers{
  public void result()
  {
    float firstNum = 2.5f;
    float secondNum = 1.5f;

    float sum = firstNum + secondNum;
    float difference = firstNum - secondNum;
    float product = firstNum * secondNum;

    System.out.println("The sum of " + firstNum + " and " + secondNum + " is " + sum);
    System.out.println("The difference of " + firstNum + " and " + secondNum + " is " + difference);
    System.out.println("The product of " + firstNum + " and " + secondNum + " is " + product);
  }
}